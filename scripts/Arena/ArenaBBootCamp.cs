function Duck::PullMA::Start(%this, %object)
{
	%playerClient = Player::getClient(%object);
	
	if($duckpullma)
	{
		Client::sendMessage(%playerClient, 0, "Please wait 10 seconds before hitting it again.~wfemale1.wbelay.wav");
		return;
	}
	
	if($TA::PullMA == true)
	{
		$duckpullma = true;
		Client::sendMessage(%playerClient, 0, "MA Bots Off~wmine_act.wav");
		$TA::PullMA = false;
		schedule("$duckpullma = false;",10);
	}
	else if($TA::PullMA == false)
	{
		$duckpullma = true;
		Client::sendMessage(%playerClient, 0, "MA Bots On~wmine_act.wav");
		$TA::PullMA = true;
		Duck::PullMA();
		schedule("$duckpullma = false;",10);
	}
}

